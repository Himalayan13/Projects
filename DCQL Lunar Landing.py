import gymnasium as gym
env = gym.make('LunarLander-v2')
state_shape = env.observation_space.shape #ref to the vector of 8 elements (8,)
state_size = env.observation_space.shape[0] #ref to the 8 state coord post coord, vel coord etc
number_actions = env.action_space.n #ref to the 4 actions
