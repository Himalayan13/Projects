"""## Part 1 - Building the AI

### Creating the architecture of the Neural Network
"""

class Network(nn.Module):
  def __init__(self, state_size, action_size, seed=42):
    super(Network, self).__init__()   #inheritance using super
    self.seed = torch.manual_seed(seed)
    self.fc1 = nn.Linear(state_size, 64)    #creating our 3 fc layers
    self.fc2 = nn.Linear(64, 64)
    self.fc3 = nn.Linear(64, action_size)
  def forward(self, state):
    x = self.fc1(state)         # sending input to the 1st fully connected layer
    x = F.relu(x)               # w the help of rectifier activating unit
    x = self.fc2(x)
    x = F.relu(x)
    return  self.fc3(x)

